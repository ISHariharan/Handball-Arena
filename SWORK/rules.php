            <h1>Rules & Regulation</h1>
            <br> </br>
            <div class="parent">
              <img src="https://thebridge.in/h-upload/2022/05/30/29169-indian-men-hockey-team.webp" style="height: 170px;width: 300px;">
            <p class="p2">
                <b>Team Constitution</b>
                <br>
                "A team consists of 10 court players and two goalkeepers out of which only seven can be in the play at any time, i.e., six court players and one goalkeeper."
            
              </br>
            </p>
          </div>
            <br> </br>
            <div class="parent">
            <p class="p2">
                <b>Substitute</b>
                <br>
                "Any number of substitutions is permitted during the game. They need not notify the time-keeper provided the player being substituted has left the field of play."
            </br>
            </p>
            <img src="OTHERS/Substitute.jpg" style="height: 170px;width: 300px;">
          </div>
            <br> </br>
            <p class="p2">
                <b>Duration</b>
                <br>
                "The game consists of two periods of 30 minutes each with a ten-minute interval for both men and women above the age of 18. For juniors the two periods are of 25 minutes each and the interval is of 10 minutes."
            </br>
            </p>
            <br> </br>
            <p class="p2">
                <b>Start</b>
                <br>
                "Each period commences with a throw-off by the team entitled to do it."
            </br>    
            </p>
            <br> </br>
            <p class="p2">
                <b>Scoring</b>
                <br>
                "Shots for goal must be executed from outside the goal area. A goal is scored if the ball has travelled (in the air or on the ground) completely over the goal line between the posts."
            </br>
            </p>
            <br> </br>
            <p class="p2">
                <b>Rules</b>
                <br>
                "Shots for goal must be executed from outside the goal area. A goal is scored if the ball has travelled (in the air or on the ground) completely over the goal line between the posts."
            </br>
            </p>
            <p class="p2">
              <br> </br>
                <p class="p4"> 
                    (1) A free throw is awarded for various infringements like a faulty handling of the ball, a faulty approach to an opponent, a faulty attitude to the referee and so on. If the place where the free throw awarded is in between the free throw line and the goal area line (9 m and 6 m lines) then the free throw must be taken from the nearest spot outside the free throw line.
                </p>
                <p class="p4"> 
                    (2) A player is allowed to hold the ball for three seconds only. They are allowed to hit the oncoming ball with the fist, but not to throw it up and fist it away
                </p>
                <p class="p4"> 
                    (3) A player holding the ball may take not more than three steps and must pass it or bounce it with one hand.
                </p>
                <p class="p4"> 
                    (4) Once the ball has been caught or bounced with one hand, no more bouncing is allowed, and it has to be passed within three seconds.
                </p>
                <p class="p4"> 
                    (5) Lobbing, hooking, hugging, tripping, in any way is forbidden.
                </p>
                <p class="p4"> 
                    (6) Indiscipline in the field or court of play or bad behaviour towards the referee is also punishable. If such offences are repeated, a player may be suspended for two minutes, disqualified or excluded.
                </p>
                <p class="p4"> 
                    (7) In three fundamental respects handball differs from football. There are no offside rules, the ball may not be played back into the goal area or to the goalkeeper and lastly no goal melee is possible as no player is allowed inside the goal area.
                </p>
                <p class="p4"> 
                    (8) Only the goalkeeper is allowed to move around freely within the goal area and to come out of the goal area without the ball.
                </p>
            </p>
