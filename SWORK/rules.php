            <P class="p2">
              <b>The Evolution of Handball (1940s-1960s)</b>
              <br>
              Throughout the mid-20th century, handball continued to grow in popularity across Europe, with several countries establishing national leagues and competitions. The sport also saw further refinements in rules and playing techniques, making it more strategic and competitive.
            </br>
            </P>
          </div>
            <br> </br>
            <div class="parent">
            <P class="p2">
              <b>Handball Goes Global (1970s-1990s)</b>
              <br>
              The 1970s and 1980s marked a significant expansion of handball's reach beyond Europe. Countries from Asia, Africa, and the Americas embraced the sport, leading to the establishment of continental handball federations. The IHF played a crucial role in promoting handball's global growth and organizing international competitions.
            </br>
            </P>
