            <h1>The History of Handball</h1>
            <br> </br>
            
            <p class="p2">
              Handball, a fast-paced and dynamic team sport, has a rich history that spans over a century. Born from the need for an indoor alternative to football, handball has evolved into an internationally recognized sport, captivating millions of players and fans worldwide. Join us on a journey through time as we explore the fascinating history of handball.
              <br> </br>
            </p>
          
          <div class="parent">
            <img src="../OTHERS/p1.jpg" style="height: 170px;width: 300px;">
            <p class="p2">
              <b>The Origins (Late 19th Century)</b>
              <br>
              Handball's origins can be traced back to the late 19th century in northern Europe. The sport's early form was played outdoors on grass, resembling a combination of soccer and rugby. The primary objective was to score goals by throwing a ball into the opposing team's net, using hands rather than feet.
            </br>
            <p>
            </div>
              <br> </br>  
              <div class="parent">
            <P class="p2">
              <b>Indoor Handball Emerges (Early 20th Century)</b>
              <br>
              The game took a significant turn in the early 20th century when it transitioned from an outdoor sport to an indoor one. The need for an indoor version arose due to harsh weather conditions during the winter months. This adaptation allowed handball to be played year-round, boosting its popularity.
            </br>
            </P>
            <img src="https://www.ihf.info/sites/default/files/inline-images/1954%20Men%27s%20WCh_1440x600.jpg" style="height: 170px;width: 300px;">
          </div>
            <br> </br>
            <div class="parent">
            <img src="../OTHERS/p3.jpg_large" style="height: 170px;width: 300px;">
            <P class="p2">
              <b>The Establishment of Rules (1920s)</b>
              <br>
              In 1926, handball took a step towards formalization when the International Amateur Handball Federation (IAHF) was formed. This organization later became the International Handball Federation (IHF). With this development, standardized rules were introduced, setting the foundation for modern handball.
            </br>
            </P>
          </div>
            <br> </br>
            <div class="parent">
            <P class="p2">
              <b>Handball at the Olympics (1936)</b>
              <br>
              Handball made its debut as an Olympic sport at the 1936 Summer Olympics held in Berlin, Germany. However, it was played as an 11-player outdoor game until the 1972 Munich Olympics, where it switched to its current indoor format with seven players on each team.
            </br>
            </P>
            <img src="https://www.sportshistoryweekly.com/uploads/original/20210610-155910-Team%20handball.png.jpg" style="height: 170px;width: 300px;">
          </div>
